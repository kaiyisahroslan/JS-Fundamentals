// Primitive

// String
const userName = 'sara';
console.log(userName);
console.log(typeof userName);

// Number
const age = 45;
console.log(age);
console.log(typeof age);

// Boolean
const isStudent = true;
console.log(isStudent);
console.log(typeof isStudent);

// Null
const car = null;
console.log(car);
console.log(typeof car);

// Undefined
let color;
console.log(color);
console.log(typeof color);

// Symbol
const sym = Symbol();
console.log(sym);
console.log(typeof sym);


// Ref types (objs)

// Array
const numbers = [1,2,3,4,5,6];
console.log(numbers);
console.log(typeof numbers);

// Object
const student = {
    firstName : 'sara',
    age : 20,
    address : {
        city : 'Kuala Lumpur',
        country : 'Malaysia'
    }
};
console.log(student);
console.log(typeof student);
